    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown

        DefaultTableModel model = (DefaultTableModel) tblqrs.getModel();
        File directory = new File(BDUtility.getPath("resources/qrCodes"));
        File[] files = directory.listFiles();

        if (files != null) {

            for (File file : files) {

                model.addRow(new Object[]{file.getName(), file.length()});

            }

        }

    }//GEN-LAST:event_formComponentShown

    private void tblqrsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblqrsMouseClicked

        int index = tblqrs.getSelectedRow();
        TableModel model = tblqrs.getModel();
        String name = model.getValueAt(index, 0).toString();
        ImageIcon icon = new ImageIcon(BDUtility.getPath("resources/qrCodes" + File.separator + name));
        Image image = icon.getImage().getScaledInstance(322, 295, Image.SCALE_SMOOTH);
        ImageIcon resizedIcon = new ImageIcon(image);

        lblimage.setIcon(resizedIcon);
    }//GEN-LAST:event_tblqrsMouseClicked

